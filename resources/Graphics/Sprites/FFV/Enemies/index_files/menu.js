function displayMenu(folder,level)
	{
	var i=0;

	if(folder == 'main')
		document.write('<BR><BR>');
	else
		{
		document.write('<center><a class="homelink" href="');
		for(i=0;i<level;i++)
			document.write('../');
		document.write('index.htm">RPG Legion Home</a></center><BR>');
		}

	if(folder == 'sprites')
		displaySprites(level);

	if(folder == 'reviews')
		displayReviews(level);

	displayGameMenu(folder,level - 1);
	displayGames(folder,level);
	if(folder != 'sprites')
		displaySpritesHeader(level);

	if(folder == 'main' || folder == 'reviews' || folder == 'other' || folder == 'sprites' || folder == 'links')
		{
		displayMisc(level);
		}
	}